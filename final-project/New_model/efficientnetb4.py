class EpochLogger(tf.keras.callbacks.Callback):
    def __init__(self, phase_name="Phase"):
        super().__init__()
        self.phase_name = phase_name
        self.history    = []

    def on_train_begin(self, logs=None):
        print(f"\n{'─'*100}")
        print(f"  {'Epoch':<8} | {'Train Acc':>10} | {'Val Acc':>10} | "
              f"{'Train Loss':>11} | {'Val Loss':>10} | {'LR':>10} | {'Gap':>8} | Status")
        print(f"{'─'*100}")

    def on_epoch_end(self, epoch, logs=None):
        logs    = logs or {}
        tr_acc  = logs.get('accuracy',     0.0)
        vl_acc  = logs.get('val_accuracy', 0.0)
        tr_loss = logs.get('loss',         0.0)
        vl_loss = logs.get('val_loss',     0.0)
        gap     = tr_acc - vl_acc

        try:
            lr = float(tf.keras.backend.get_value(
                       self.model.optimizer.learning_rate))
        except Exception:
            lr = 0.0

        if   gap >  0.10: status = "🔴 OVERFIT"
        elif gap >  0.05: status = "🟡 Mild OVF"
        elif gap < -0.05: status = "🔵 Underfit"
        else:             status = "✅ OK"

        self.history.append(dict(epoch=epoch+1, tr_acc=tr_acc, vl_acc=vl_acc,
                                 tr_loss=tr_loss, vl_loss=vl_loss, gap=gap, lr=lr))
        print(f"  {self.phase_name}-{epoch+1:<4} | {tr_acc:>10.4f} | {vl_acc:>10.4f} | "
              f"{tr_loss:>11.4f} | {vl_loss:>10.4f} | {lr:>10.2e} | {gap:>+8.4f} | {status}")

    def on_train_end(self, logs=None):
        print(f"{'─'*100}")
        if not self.history: return
        max_gap = max(r['gap'] for r in self.history)
        best_vl = max(r['vl_acc'] for r in self.history)
        best_ep = next(r['epoch'] for r in self.history if r['vl_acc'] == best_vl)
        print(f"\n  📌 [{self.phase_name}] Best Val Acc: {best_vl:.4f} (ep {best_ep}) | "
              f"Max Gap: {max_gap:+.4f} ", end="")
        if   max_gap > 0.10: print("→ 🔴 Overfitting!")
        elif max_gap > 0.05: print("→ 🟡 Mild Overfitting")
        else:                print("→ ✅ Good generalisation")

