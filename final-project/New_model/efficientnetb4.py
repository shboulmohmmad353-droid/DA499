def standardize_images(target_dir, target_size=(380, 380)):
    print(f"\n🔄 Standardizing: {target_dir}")
    converted, skipped = 0, 0
    for root, _, files in os.walk(target_dir):
        for file in files:
            if not file.lower().endswith(
                    ('.png', '.jpg', '.jpeg', '.bmp', '.gif', '.webp')):
                continue
            fp = os.path.join(root, file)
            try:
                with Image.open(fp) as img:
                    img      = img.convert('RGB').resize(target_size)
                    new_path = os.path.splitext(fp)[0] + ".jpg"
                    img.save(new_path, "JPEG", quality=95)
                if fp != new_path:
                    os.remove(fp)
                converted += 1
            except Exception as e:
                print(f"  ⚠️ Skipping {file}: {e}")
                skipped += 1
    print(f"  ✅ converted={converted} | skipped={skipped}")


def balance_dataset(target_dir, target_count=1500):
    print(f"\n⚖️  Balancing: {target_dir}  (target={target_count})")
    for cls in CLASS_NAMES:
        cls_path = os.path.join(target_dir, cls)
        if not os.path.exists(cls_path):
            print(f"  ⚠️ Not found: {cls_path}")
            continue
        files = [os.path.join(cls_path, f)
                 for f in os.listdir(cls_path)
                 if f.lower().endswith('.jpg')]
        print(f"  [{cls}] before: {len(files)}")
        if cls == 'Non_fractured' and len(files) > target_count:
            for f in random.sample(files, len(files) - target_count):
                os.remove(f)
        elif cls == 'Fractured' and len(files) < target_count:
            for i in range(target_count - len(files)):
                src  = random.choice(files)
                dest = os.path.join(cls_path, f"aug_{i}_{os.path.basename(src)}")
                shutil.copy2(src, dest)
        print(f"  [{cls}] after : {len(os.listdir(cls_path))}  ✅")


def dataset_stats(directory, label=""):
    print(f"\n📊 Stats — {label}")
    total = 0
    for cls in CLASS_NAMES:
        p = os.path.join(directory, cls)
        if os.path.exists(p):
            n = len(os.listdir(p)); total += n
            print(f"  {cls:<20}: {n:>5}")
    print(f"  {'TOTAL':<20}: {total:>5}")
    return total

