def predict_image(image_path, threshold=0.5, show=True):
    img        = Image.open(image_path).convert('RGB').resize(IMG_SIZE)
    arr        = np.expand_dims(np.array(img, dtype=np.float32), axis=0)
    prob       = float(model.predict(arr, verbose=0)[0][0])
    pred_class = int(prob > threshold)
    label      = CLASS_NAMES[pred_class]
    confidence = prob if pred_class == 1 else 1 - prob

    if show:
        color = 'red' if label == 'Fractured' else 'green'
        plt.figure(figsize=(5, 5))
        plt.imshow(img); plt.axis('off')
        plt.title(f"Prediction: {label}\nProb: {prob:.4f} | Confidence: {confidence:.2%}",
                  fontsize=12, color=color, fontweight='bold')
        plt.tight_layout(); plt.show()

    print(f"\n  🩻 {os.path.basename(image_path)}")
    print(f"  🏷️  Label: {label} | Prob: {prob:.4f} | Conf: {confidence:.2%}")
    return dict(label=label, probability=prob, confidence=confidence)


