def plot_learning_curves(h1, h2):
    def merge(key):
        return h1.history.get(key, []) + h2.history.get(key, [])

    tr_acc  = merge('accuracy');    vl_acc  = merge('val_accuracy')
    tr_loss = merge('loss');        vl_loss = merge('val_loss')
    tr_auc  = merge('auc');         vl_auc  = merge('val_auc')
    tr_prec = merge('precision');   vl_prec = merge('val_precision')
    p1_end  = len(h1.history['loss'])
    epochs_r = range(1, len(tr_acc) + 1)

    # ── جدول كامل ─────────────────────────────────────────────────────
    print("\n" + "="*95)
    print("  📜  SECTION 18 — Full Training Log")
    print("="*95)
    print(f"  {'Ep':<5}|{'Ph':<3}|{'Tr Acc':>8}|{'Vl Acc':>8}|"
          f"{'Tr Loss':>9}|{'Vl Loss':>9}|{'AUC Tr':>7}|{'AUC Vl':>7}|{'Gap':>8}| Status")
    print("-"*95)

    for i in range(len(tr_acc)):
        ph  = "P1" if i < p1_end else "P2"
        gap = tr_acc[i] - vl_acc[i]
        s   = ("🔴 OVERFIT" if gap > 0.10 else
               "🟡 Mild OVF" if gap > 0.05 else
               "🔵 Underfit" if gap < -0.05 else "✅ OK")
        auc_tr = tr_auc[i] if i < len(tr_auc) else 0
        auc_vl = vl_auc[i] if i < len(vl_auc) else 0
        print(f"  {i+1:<5}|{ph:<3}|{tr_acc[i]:>8.4f}|{vl_acc[i]:>8.4f}|"
              f"{tr_loss[i]:>9.4f}|{vl_loss[i]:>9.4f}|"
              f"{auc_tr:>7.4f}|{auc_vl:>7.4f}|{gap:>+8.4f}| {s}")
    print("="*95)

    max_gap = max(a - v for a, v in zip(tr_acc, vl_acc))
    print(f"\n  Overfitting verdict (max gap={max_gap:+.4f}): ", end="")
    if   max_gap > 0.10: print("🔴 Overfitting")
    elif max_gap > 0.05: print("🟡 Mild Overfitting")
    else:                print("🟢 Good — no significant overfitting")

    # ── Plots ─────────────────────────────────────────────────────────
    fig, axes = plt.subplots(2, 2, figsize=(18, 12))
    fig.suptitle('Learning Curves — Training vs Validation',
                 fontsize=17, fontweight='bold')

    def draw(ax, tr, vl, title, ylabel):
        ax.plot(epochs_r, tr, 'o-', color='royalblue',  lw=2, label='Train')
        ax.plot(epochs_r, vl, 'o-', color='darkorange', lw=2, label='Validation')
        ax.axvline(x=p1_end+0.5, color='green', ls='--', lw=1.8,
                   label='Phase-2 start')
        ax.fill_between(epochs_r,
                        [min(a,b) for a,b in zip(tr,vl)],
                        [max(a,b) for a,b in zip(tr,vl)],
                        alpha=0.12, color='crimson', label='Gap')
        ax.set_title(title, fontsize=13, fontweight='bold')
        ax.set_xlabel('Epoch'); ax.set_ylabel(ylabel)
        ax.legend(); ax.grid(True, alpha=0.3)

    draw(axes[0,0], tr_acc,  vl_acc,  'Accuracy',  'Accuracy')
    draw(axes[0,1], tr_loss, vl_loss, 'Loss',      'Loss')
    if tr_auc:  draw(axes[1,0], tr_auc,  vl_auc,  'AUC',       'AUC')
    if tr_prec: draw(axes[1,1], tr_prec, vl_prec, 'Precision', 'Precision')

    plt.tight_layout()
    plt.savefig(os.path.join(base_path, "learning_curves.png"),
                dpi=150, bbox_inches='tight')
    plt.show()
    print("🖼️  Saved → learning_curves.png")


