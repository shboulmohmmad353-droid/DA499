def build_model():
    """
    ✅ إصلاح رئيسي: Functional API مع training=False للـ base model
    هذا يحل مشكلة BatchNorm في EfficientNet عند التجميد —
    يضمن أن BN تعمل في inference mode بشكل صريح،
    فتتطابق نتائج التدريب مع التسريب.

    ✅ إصلاح ثانوي: تقليل حجم الـ head وتخفيف Dropout و L2
    لأن الـ head الضخم مع dropout عالي = تعلم بطيء جداً
    """
    # ── Base Model ────────────────────────────────────────────────────
    base_model = tf.keras.applications.EfficientNetB4(
        include_top=False,
        weights='imagenet',
        input_shape=(380, 380, 3)
    )
    base_model.trainable = False

    # ── Functional API (الحل الصحيح لمشكلة BN) ───────────────────────
    inputs = tf.keras.Input(shape=(380, 380, 3), name="input_image")

    # training=False → BN في base تعمل بـ inference mode دائماً في Phase 1
    x = base_model(inputs, training=False)

    # ── Spatial Aggregation ───────────────────────────────────────────
    x = tf.keras.layers.GlobalAveragePooling2D()(x)
    x = tf.keras.layers.BatchNormalization()(x)

    # ── Block 1: 1024 ─────────────────────────────────────────────────
    x = tf.keras.layers.Dense(
            1024, activation='relu',
            kernel_regularizer=tf.keras.regularizers.l2(1e-5))(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.Dropout(0.35)(x)

    # ── Block 2: 1024 ─────────────────────────────────────────────────
    x = tf.keras.layers.Dense(
            1024, activation='relu',
            kernel_regularizer=tf.keras.regularizers.l2(1e-5))(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.Dropout(0.30)(x)

    # ── Block 3: 512 ──────────────────────────────────────────────────
    x = tf.keras.layers.Dense(
            512, activation='relu',
            kernel_regularizer=tf.keras.regularizers.l2(1e-5))(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.Dropout(0.25)(x)

    # ── Block 4: 512 ──────────────────────────────────────────────────
    x = tf.keras.layers.Dense(
            512, activation='relu',
            kernel_regularizer=tf.keras.regularizers.l2(1e-5))(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.Dropout(0.20)(x)

    # ── Block 5: 256 ──────────────────────────────────────────────────
    x = tf.keras.layers.Dense(
            256, activation='relu',
            kernel_regularizer=tf.keras.regularizers.l2(1e-5))(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.Dropout(0.15)(x)

    # ── Block 6: 256 ──────────────────────────────────────────────────
    x = tf.keras.layers.Dense(
            256, activation='relu',
            kernel_regularizer=tf.keras.regularizers.l2(1e-5))(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.Dropout(0.15)(x)

    # ── Block 7: 128 ──────────────────────────────────────────────────
    x = tf.keras.layers.Dense(
            128, activation='relu',
            kernel_regularizer=tf.keras.regularizers.l2(1e-5))(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.Dropout(0.10)(x)

    # ── Block 8: 64 ───────────────────────────────────────────────────
    x = tf.keras.layers.Dense(
            64, activation='relu',
            kernel_regularizer=tf.keras.regularizers.l2(1e-5))(x)
    x = tf.keras.layers.BatchNormalization()(x)

    # ── Output ────────────────────────────────────────────────────────
    outputs = tf.keras.layers.Dense(1, activation='sigmoid', name="output")(x)

    model = tf.keras.Model(inputs, outputs, name="FractureNet_B4_Fixed")
    return model, base_model


