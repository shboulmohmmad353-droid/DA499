# ===================== 11. Pipeline Optimization =====================
AUTOTUNE = tf.data.AUTOTUNE

train_ds = train_ds.cache().shuffle(1000).prefetch(buffer_size=AUTOTUNE)
val_ds = val_ds.cache().prefetch(buffer_size=AUTOTUNE)
test_ds = test_ds.cache().prefetch(buffer_size=AUTOTUNE)

# ===================== 12. Data Augmentation (Improved) =====================
data_augmentation = tf.keras.Sequential([
    tf.keras.layers.RandomFlip("horizontal"),
    tf.keras.layers.RandomFlip("vertical"),
    tf.keras.layers.RandomRotation(0.2),
    tf.keras.layers.RandomZoom(0.2),
    tf.keras.layers.RandomContrast(0.2),
    tf.keras.layers.RandomBrightness(0.2),
])


def augment(images, labels):
    return data_augmentation(images), labels


train_ds = train_ds.map(augment)

# ===================== 13. Build Model (Improved Head) =====================
base_model = tf.keras.applications.EfficientNetB0(
    include_top=False,
    weights='imagenet',
    input_shape=(224, 224, 3)
)
base_model.trainable = False

x = base_model.output

x = tf.keras.layers.GlobalAveragePooling2D()(x)
x = tf.keras.layers.BatchNormalization()(x)

x = tf.keras.layers.Dense(256, activation='relu')(x)
x = tf.keras.layers.Dropout(0.4)(x)

x = tf.keras.layers.Dense(64, activation='relu')(x)
x = tf.keras.layers.Dropout(0.3)(x)

output = tf.keras.layers.Dense(1, activation='sigmoid')(x)
model = tf.keras.Model(inputs=base_model.input, outputs=output)
model.summary()

# ===================== 14. Compute Class Weights (Dynamic) =====================
# IMPORTANT: image_dataset_from_directory sorts classes ALPHABETICALLY
# So: Fractured = class 0, Non_fractured = class 1
print("Class mapping:", class_names)

train_fractured = len(os.listdir(os.path.join(train_dir, 'Fractured')))
train_non_fractured = len(os.listdir(os.path.join(train_dir, 'Non_fractured')))
total_samples = train_fractured + train_non_fractured

weight_for_0 = total_samples / (2 * train_fractured)       # Fractured (class 0)
weight_for_1 = total_samples / (2 * train_non_fractured)   # Non_fractured (class 1)
class_weight = {0: weight_for_0, 1: weight_for_1}
print(f"Train samples: Fractured={train_fractured}, Non_fractured={train_non_fractured}")
print(f"Class weights: Fractured(0)={weight_for_0:.2f}, Non_fractured(1)={weight_for_1:.2f}")

# ===================== 15. Training Phase 1 (with Callbacks) =====================
early_stopping = tf.keras.callbacks.EarlyStopping(
    monitor='val_loss',
    patience=5,
    restore_best_weights=True
)

reduce_lr = tf.keras.callbacks.ReduceLROnPlateau(
    monitor='val_loss',
    factor=0.5,
    patience=3,
    min_lr=1e-7
)

model.compile(
    optimizer=tf.keras.optimizers.Adam(learning_rate=0.0001),
    loss='binary_crossentropy',
    metrics=['accuracy']
)

history = model.fit(
    train_ds,
    validation_data=val_ds,
    epochs=30,
    class_weight=class_weight,
    callbacks=[early_stopping, reduce_lr]
)

# ===================== 16. Fine-Tuning (with Callbacks) =====================
base_model.trainable = True

for layer in base_model.layers[:-30]:
    layer.trainable = False

early_stopping_fine = tf.keras.callbacks.EarlyStopping(
    monitor='val_loss',
    patience=3,
    restore_best_weights=True
)

reduce_lr_fine = tf.keras.callbacks.ReduceLROnPlateau(
    monitor='val_loss',
    factor=0.5,
    patience=2,
    min_lr=1e-8
)

model.compile(
    optimizer=tf.keras.optimizers.Adam(1e-5),
    loss='binary_crossentropy',
    metrics=['accuracy']
)

history_fine = model.fit(
    train_ds,
    validation_data=val_ds,
    epochs=10,
    class_weight=class_weight,
    callbacks=[early_stopping_fine, reduce_lr_fine]
)

# ===================== 17. Evaluate on Validation Set =====================
y_true = []
y_pred = []

for images, labels in val_ds:
    preds = model.predict(images)
    preds = (preds > 0.5).astype(int)

    y_true.extend(labels.numpy())
    y_pred.extend(preds.flatten())

print("=== Validation Set Results ===")
print(classification_report(y_true, y_pred))

