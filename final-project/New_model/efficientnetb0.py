# ===================== 22. Prediction Function =====================
import cv2


def predict(img_path):
    img = cv2.imread(img_path)
    img = cv2.resize(img, (224, 224))
    img = img / 255.0
    img = np.expand_dims(img, axis=0)

    pred = model.predict(img)[0][0]
    return "Fractured" if pred > 0.5 else "Normal"


